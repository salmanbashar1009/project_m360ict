class Ticket {
  final String id;
  final String title;
  final String date;
  final String status;
  final double price;
  final String priority;
  final String name;

  Ticket({
    required this.id,
    required this.title,
    required this.date,
    required this.status,
    required this.price,
    required this.priority,
    required this.name
  });
}